/**
 * Structural design patterns are concerned with how classes and objects can be
 * composed, to form larger structures.
 * The structural design patterns simplifies the structure by identifying the
 * relationships.
 * These patterns focus on, how the classes inherit from each other and how they
 * are composed from other classes.<br>
 * There are following 7 types of structural design patterns:
 * <ol>
 * <li>Adapter Pattern: Adapting an interface into another according to client expectation.</li>
 * <li>Bridge Pattern: Separating abstraction (interface) from implementation.</li>
 * <li>Composite Pattern: Allowing clients to operate on hierarchy of objects.</li>
 * <li>Decorator Pattern: Adding functionality to an object dynamically.</li>
 * <li>Facade Pattern: Providing an interface to a set of interfaces.</li>
 * <li>Flyweight Pattern: Reusing an object by sharing it.</li>
 * <li>proxy Pattern: Representing another object.</li>
 * </ol>
 * 
 * @author ciro
 *
 */
