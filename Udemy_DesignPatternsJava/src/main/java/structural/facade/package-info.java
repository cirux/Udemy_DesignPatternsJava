
/**
 * A Facade Pattern says that just "just provide a unified and simplified
 * interface to a set of interfaces in a subsystem, therefore it hides the
 * complexities of the subsystem from the client".
 * 
 * In other words, Facade Pattern describes a higher-level interface that makes
 * the sub-system easier to use.
 * 
 * Practically, every Abstract Factory is a type of Facade.
 * 
 * Advantages: It shields the clients from the complexities of the sub-system
 * components. It promotes loose coupling between subsystems and its clients.
 * <br>
 * It is used:
 * 
 * When you want to provide simple interface to a complex sub-system. <br>
 * When several dependencies exist between clients and the implementation
 * classes of an abstraction.
 * 
 * @author ciro
 *
 */
